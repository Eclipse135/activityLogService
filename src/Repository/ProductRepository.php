    public function findRandom(): Product
    {
        $result = $this->createQueryBuilder('p')
            ->select('p.id')
            ->getQuery()
            ->getResult();

        $allIds = array_column($result, 'id');

        $randomId = $allIds[array_rand($allIds)];

        return $this->find($randomId);
    }

